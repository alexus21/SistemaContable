            // Llena la tabla con los datos de la base de datos
            int i = 0;
            while (rs.next()) {
                Object[] fila = {
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5)
                };
                tableModel.addRow(fila);
                jTable1.setRowHeight(30);
            }
